    public void getTrackNo(){
        try{
            String sql = "select * from laps where status = '"+str+"'";
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/acps","root","root");
            
            pst = conn1.prepareStatement(sql);
            rs = pst.executeQuery();
            if(rs.next()){
                String add1 = rs.getString("lapn");
                jTextField4.setText(add1);
                String add2 = rs.getString("trackn");
                jTextField5.setText(add2);
            }
         
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try{
                rs.close();
                pst.close();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }
    
