    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        // Store the car parrking details in dataBase
           String sql = "insert into parked (`rc`,`name`,`gender`,`position`,`brand`,`issue`,`expiry`,`lapn`,`trackn`) values (?,?,?,?,?,?,?,?,?)";
           try{
           
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/acps","root","root");
            PreparedStatement pst = conn1.prepareStatement(sql);
            
            pst.setString(1, jTextField1.getText());        // for rc 
            pst.setString(2, jTextField2.getText());        //for name
            
            jRadioButton1.setActionCommand("Male");         // for male Radio button
            jRadioButton2.setActionCommand("Female");       //for female radio button
            pst.setString(3, buttonGroup1.getSelection().getActionCommand());
                       
            pst.setString(4, jTextField3.getText());        // for position            
            pst.setString(5,(String) jComboBox1.getSelectedItem());     // for select given item
            
            pst.setString(6, ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText());        // for issue date
            pst.setString(7, ((JTextField)jDateChooser2.getDateEditor().getUiComponent()).getText());         // for expiry date
            
            pst.setString(8, jTextField4.getText());        // for lapn 
            pst.setString(9, jTextField5.getText());        // for trackn
            
            pst.executeUpdate();
            updateData();
            clear();
            getTrackNo();
            JOptionPane.showMessageDialog(null, "car parked.");
            
        }catch(SQLException e){
            e.printStackTrace();
            //JOptionPane.showMessageDialog(null, e);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(NewParking.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

